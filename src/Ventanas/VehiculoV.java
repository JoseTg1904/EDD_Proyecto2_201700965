    private void modificarVehiculo(){
        if(vehiculo != null){
            if(marcaMod.getText().length() > 0){
                vehiculo.setMarca(marcaMod.getText().trim());
            }
            if(modeloMod.getText().length() > 0){
                vehiculo.setModelo(modeloMod.getText().trim());
            }
            if(anioMod.getText().length() > 0){
                vehiculo.setAnio(anioMod.getText().trim());
            }
            if(colorMod.getText().length() > 0){
                vehiculo.setColor(colorMod.getText().trim());
            }
            if(precioMod.getText().length() > 0){
                vehiculo.setPrecio(precioMod.getText().trim());
            }
            vehiculo.setTransmision((String)tranMod.getSelectedItem());
        }
    }

    private void crearVehiculo(){
        String transmision = (String)tranCre.getSelectedItem();
        Inicial.vehiculos.insertarNormal(new Vehiculo(placaCre.getText().trim(), marcaCre.getText().trim(), modeloCre.getText().trim(), 
        anioCre.getText().trim(), colorCre.getText().trim(), precioCre.getText().trim(),transmision));
        JOptionPane.showMessageDialog(this,"Se a agregado el vehiculo al sistema exitosamente!");
    }
    
    public void listadoPlacas(){
        comboMod.removeAllItems();
        ArrayList<String> placas = Inicial.vehiculos.devolverPlacas();
        for(int i = 0; i < placas.size(); i++){
            comboMod.addItem(placas.get(i));
        }
    }
    
    private void cargaMasivaVehiculos(File archivoVehiculos) throws FileNotFoundException, IOException{
        FileReader lector = new FileReader(archivoVehiculos);
        BufferedReader almacenador = new BufferedReader(lector);
        String linea, contenido = "";
        String[] aux, aux1;
        while( (linea = almacenador.readLine()) != null ){
            contenido += linea;
        }
        aux1 = contenido.split(";");
        for(int i = 0; i < aux1.length; i++){
            aux = aux1[i].split(":");
            Inicial.vehiculos.insertarNormal(new Vehiculo(aux[0].trim(), aux[1].trim(), aux[2].trim(),aux[3].trim(), aux[4].trim(), aux[5].trim(), aux[6].trim()));
            System.out.println(Inicial.vehiculos.devolverGrafo());
        }
        JOptionPane.showMessageDialog(this,"Se han cargado los vehiculos exitosamente!\nPuede continuar con la ejecucion del programa");
        listadoPlacas();
        System.out.println(Inicial.vehiculos.devolverGrafo());
    }
    
